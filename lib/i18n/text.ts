type Primitive = string | number | boolean | null | undefined;

type DeepWiden<T> = T extends (...args: infer A) => infer R
  ? (...args: A) => R
  : T extends readonly (infer U)[]
    ? DeepWiden<U>[]
    : T extends Primitive
      ? T extends string
        ? string
        : T extends number
          ? number
          : T extends boolean
            ? boolean
            : T
      : T extends object
        ? { [K in keyof T]: DeepWiden<T[K]> }
        : T;

type LocaleText = DeepWiden<typeof text.ar> & DeepWiden<typeof text.en>;

function isPlainObject(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function mergeLocaleText(
  fallback: Record<string, unknown>,
  override: Record<string, unknown>
): Record<string, unknown> {
  const result: Record<string, unknown> = {};
  const keys = new Set([...Object.keys(fallback), ...Object.keys(override)]);

  for (const key of keys) {
    const fallbackValue = fallback[key];
    const overrideValue = override[key];

    if (isPlainObject(fallbackValue) && isPlainObject(overrideValue)) {
      result[key] = mergeLocaleText(fallbackValue, overrideValue);
      continue;
    }

    result[key] = overrideValue === undefined ? fallbackValue : overrideValue;
  }

  return result;
}

const cachedText: Partial<Record<AppLocale, LocaleText>> = {};

export function getText(locale: AppLocale): LocaleText {
  const cached = cachedText[locale];
  if (cached) {
    return cached;
  }

  const merged = (
    locale === "ar"
      ? mergeLocaleText(
          text.en as unknown as Record<string, unknown>,
          text.ar as unknown as Record<string, unknown>
        )
      : mergeLocaleText(
          text.ar as unknown as Record<string, unknown>,
          text.en as unknown as Record<string, unknown>
        )
  ) as LocaleText;

  cachedText[locale] = merged;
  return merged;
}
